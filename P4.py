
inch = float(input("Enter measurement in inches: "))

foot = inch / 12
yard = inch / 36
cm = inch * 2.54
meter = inch / 39.37


print("Measurement in foot =", round(foot, 2))
print("Measurement in yard =", round(yard, 2))
print("Measurement in centimetre =", round(cm, 2))
print("Measurement in meter =", round(meter, 2))
